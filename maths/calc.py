a = float(input("enter first number :"))
b = float(input("enter second number :"))
operator = input("enter the action :")
if operator == "add":
    print(a+b)
if operator == "sub":
    print(a-b)
if operator == "div":
    print(a/b)
if operator == "mul":
    print(a*b)
if operator == "power":
    print(a**b)
if operator == "remain":
    print(a%b)
